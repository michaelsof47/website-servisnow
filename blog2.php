        <h1>5 Aksesoris Buat Laptop Kamu Jadi Kekinian</h1>
        
        <h2>
            Tips - 10 Desember 2017 - Oleh : David Chandra Tjam
        </h2>
        
        <br>
        
        <p align="center"><img src="img/artikel2.png" width="1000px;" height="500px;"></p>
        
        <br><br>
        
        <p>Kekinian bukanlah kata-kata yang asing bagi orang-orang yang selalu update akan hal baru. Kalau orangnya aja sudah kekinian, malu dong klo laptopnya masih ketinggalan zaman. Tenang guys, ga ada yang maksa kalian buat beli laptop seperti gambar diatas karena laptop kamu pun bisa dibuat kekinian dengan 5 aksesoris yang paling dibutuhkan buat laptop zaman now. Berikut adalah beberapa tips dari servisnow.com
        </p>
        
        <ol>
            <li>
                Hardisk External<br>
                <p>Memori laptopmu punya kapasitas yang terbatas, tetapi aktifitasmu banyak. Oleh karena itu, sedia selalu memori penyimpanan cadangan seperti hardisk. Banyak data yang dirasa kurang dibutuhkan dapat dipindahkan ke hardisk tersebut. Selain itu, kamu juga dapat menggunakan hardisk untuk memback-up file-file di laptopmu. Jadi gak ada lagi deh sekarang ceritanya bigung kalau data hilang gara-gara virus atau keformat. Selain itu, kamu juga harus sedia media penyimpanan yang kecil seperti flashdisk yang pastinya ramping dan sangat mudah dibawa kemana-mana.</p>
            </li>
            <br>
            <li>
                Screen Guard<br>
                <p>Layar laptop merupakan salah satu yang paling penting bagi laptop. Agar layar laptop kamu tetap bagus seperti baru, pakailah screen guard dan bersihkan layar laptop dengan kain microfiber secara berkala. Jika screen guard laptop kamu sudah rusak, jangan segan-segan untuk memasang lagi screen guard yang baru. Harganya ga mahal kok, jadi jangan peliit untuk mengganti yang baru daripada menyesal kemudian.</p>
            </li>
            <br>
            <li>
                Speaker<br> 
                <p>Terutama bagi pelajar seperti mahasiswa sangat penting untuk mendengarkan musik pada saat mengerjakan tugas. Speaker laptop terkadang tidak dapat menghasilkan suara yang jernih untuk mendengarkan musik. Untuk itu, speaker external sangat dibutuhkan, atau jika kamu sendirian aja bisa pakai headphone unruk mendengarkan musik.</p>
            </li>
            <br>
            <li>
                Modem Portable<br>
                <p>Laptop tanpa bisa akses Internet itu nyesek banget. Apalagi kalau sudah pergi ke kafe yang ada wi-finya tapi lemot. Rasanya tuh sudah kepingin banget tuh nonjok baristanya. Makanya selalu sedia Plan B dengan membawa modem portable. Modem portable membuat akses Internet sangat mudah dan dapat dipakai bersamaan dengan gadget lainnya. Selain itu, kamu gak perlu cari kafe dan memesan minuman yang mahal untuk dapat mengakses Internet. Harga yang ditawarkan oleh provider Internet juga sangat kompetitif dan terjangkau.</p>

            </li>
            <li>
                Mouse<br>
                <p>Bagi yang sudah biasa menggunakan Personal computer (PC) tentunya tidak biasa menggunakan laptop tanpa mouse. Kehadiran benda ini juga sangat membantu aktifitas kamu khususnya bagi kamu yang berprofesi sebagai seorang designer yang sangat mementingkan presisi. Dengan kehadiran mouse portable dan terlebih lagi dengan adanya mouse wireless sangatlah mudah menggunakan laptop dibandingkan dengan memakai mouse pad.
                </p>
            </li>
        </ol>
        
        <p>
