        <h1 align="center">5 Hal Penting Untuk Merawat Laptop Kesayanganmu</h1>
        
        <h2 align="center">
            Tips - 2 November 2017 - Oleh : David Chandra Tjam
        </h2>
        
        <br>
        
        <p align="center"><img src="img/artikel1.png" width="1000px;" height="500px;"></p>
        
        <br><br>
        
        <p>Zaman sekarang siapa sih yang ga punya laptop? Apa lagi anak muda di zaman serba gadget gini pasti punya. Apa lagi klo rusak menjelang skripsi itu rasanya gimana gitu. Lebih lagi yang lagi kerja, bos sudah marah-marah buat kerja target, eh malah rusak laptopnya. Makanya servisnow.com kali ini akan membahas bagaimana caranya agar laptop kamu bakalan tahan lama dan dijamin laptopnya gak bakalan cepet rusak. Silahkan dibaca baik-baik 5 hal penting untuk merawat laptop kesayanganmu.
        </p>
        
        <ol>
            <li>
                Jangan memakai laptop dekat dengan minumanmu
                <p>Mungkin ini tidak dapat dihindari karena minuman merupakan ‘rekan’ kerja yang selalu mendampingi selain makanan pada saat kamu menggunakan laptop. Sebenarnya gak ada masalah sih soalnya minumannya gak menimbulkan radiasi kok :D. Cuma disarankan supaya minuman tersebut tidak tumpah ke laptop. Kalau sudah tumpah itu ribet soalnya buat benerin soalnya gak tahu bagian mana yang rusak. Mending daripada laptopnya basah kuyup dan resiko rusaknya laptop besar, jauhkan minuman tersebut atau berhati-hati agar tidak sampai tumpah.</p>
            </li>
            <br>
            <li>
                Jangan terlalu sering dipaksa dengan applikasi berat<br>
                <p>Menggunakan aplikasi berat tanpa memperhatikan kondisi laptop terus-menerus dapat membuat laptop kamu jadi tua dan lemot. Ini termasuk juga memainkan game dengan grafik yang tinggi dan memakan memori RAM yang cukup signifikan. Kamu mesti tahu dulu spesifikasi laptopnya sehingga bisa tahu batasan memakai aplikasi agar tidak cepat rusak.</p>
            </li>
            <br>
            <li>
                Jangan mengunduh sembarangan konten dari Internet.<br> 
                <p>Ini dimaksudkan karena banyak sekali virus yang dapat menyerang laptop kamu tanpa sadar. Konten-konten itu mungkin terlihat menarik untuk didownload, tapi kamu juga harus pilih dan mendownload yang terpercaya saja. Cek terlebuh dahulu di search engine agar tahu kalua konten yang didownload memang terpercaya.</p>
            </li>
            <br>
            <li>
                Perhatikan suhu laptop kamu
                <p>Hal ini sering kali tidak dihiraukan oleh pengguna laptop, apa lagi kalua sedang asyik main game. Game-game berat sering kali membuat laptop cepat panas atau biasa disebut overheat yang menyebabkan laptop kamu mengalami kenaikan suhu yang signifikan. Ini dapat membuat bagian-bagian dalam dari laptopmu rusak dikarenakan suhu yang tinggi bahkan bias sampai 1000. Kamu disarankanuntuk membeli aksesoris laptop seperti fan cooler sehingga laptopmu bias tetap di suhu yang stabil.</p>

            </li>
            <li>
                Jangan lupa bersihkan laptopmu<br>
                <p>Yang terakhir ini paling sering kita semua anggap sepele, padahal banyak juga laptop yang rusak cuman gara-gara pemiliknya kurang perhatian sama laptopnya sendiri. Kotoran dan debu dapat menyumbat beberapa bagian dari laptop yang bias menyebabkan laptop tidak berfungsi dengan baik. Beberapa diantaranya seperti pengeras suara dari laptop yang tidak terdengar, keybord yang rusak dan layar lcd yang kurang terang. Oleh karena itu, bersihkan laptopmu sekurang-kurangnya satu minggu sekali dengan kain seperti kain microfiber yang bisa kamu dapatkan di banyak toko aksesoris laptop.
                </p>
            </li>
        </ol>
        
